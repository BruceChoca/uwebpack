const getStyleLoaders = (cssOptions, preProcessor) => {
  const loaders = [
    require.resolve('style-loader'),
    {
      loader: require.resolve('css-loader'),
      options: cssOptions,
    },
  ]

  if (preProcessor) {
    loaders.push(require.resolve(preProcessor));
  }
  return loaders;
}



